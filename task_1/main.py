# Запрашиваем у пользователя число
n = int(input("Введите число: "))  # Вводим число и сразу переводим его в целое (int), чтобы можно было использовать в цикле

# Проходим по всем числам от 0 до введённого пользователем включительно
for i in range(0, n + 1):  # range(0, n + 1) создаёт последовательность чисел от 0 до n включительно
    # Проверяем, является ли текущее число чётным
    if i % 2 == 0:  # Если остаток от деления на 2 равен нулю — число чётное
        # Выводим чётное число
        print(i)  # Показываем текущее чётное число на экране