# Вводим строки через пробел
words = input("Введите слова через пробел: ").split()  # Получаем список строк

# Создаём новый список, где каждая строка повторяется трижды
triple_words = [word * 3 for word in words]  # Генератор списков: умножаем каждую строку на 3

# Выводим результат
print("Список с тройными словами:", triple_words)