# Запрашиваем у пользователя число n
n = int(input("Введите число n: "))  # Получаем ввод и превращаем его в целое число

# Создаём переменную для суммы чисел
total = 0  # Изначально сумма равна 0

# Проходим по всем числам от 1 до n включительно
for i in range(1, n + 1):  # range(1, n+1) создаёт числа от 1 до n
    total += i  # Добавляем текущее число i к общей сумме

# Выводим результат
print("Сумма чисел от 1 до", n, "равна:", total)  # Показываем итоговую сумму пользователю