# Запрашиваем у пользователя число
n = int(input("Введите число: "))  # Получаем ввод и переводим его в целое число

# Начинаем счёт с 1
i = 1  # Создаём переменную-счётчик, которая будет увеличиваться на каждом шаге

# Запускаем цикл, который будет работать, пока i не превысит n
while i <= n:  # Условие цикла: выполняется, пока i меньше или равно введённому числу
    print(i)  # Выводим текущее значение i
    i += 1    # Увеличиваем i на 1, чтобы перейти к следующему числу